package com.xiaokasidi.entity;

public class Administrator {
    private Integer administratorId;
    private String administratorName;
    private String administratorPhone;
    private String administratorMailbox;

    public Integer getAdministratorId() {
        return administratorId;
    }

    public void setAdministratorId(Integer administratorId) {
        this.administratorId = administratorId;
    }

    public String getAdministratorName() {
        return administratorName;
    }

    public void setAdministratorName(String administratorName) {
        this.administratorName = administratorName;
    }

    public String getAdministratorPhone() {
        return administratorPhone;
    }

    public void setAdministratorPhone(String administratorPhone) {
        this.administratorPhone = administratorPhone;
    }

    public String getAdministratorMailbox() {
        return administratorMailbox;
    }

    public void setAdministratorMailbox(String administratorMailbox) {
        this.administratorMailbox = administratorMailbox;
    }
}
