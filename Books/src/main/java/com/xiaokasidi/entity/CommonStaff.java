package com.xiaokasidi.entity;

public class CommonStaff {
    private Integer CommonStaffId;
    private String CommonStaffName;
    private String CommonStaffSex;
    private String CommonStaffPhone;

    public Integer getCommonStaffId() {
        return CommonStaffId;
    }

    public void setCommonStaffId(Integer commonStaffId) {
        CommonStaffId = commonStaffId;
    }

    public String getCommonStaffName() {
        return CommonStaffName;
    }

    public void setCommonStaffName(String commonStaffName) {
        CommonStaffName = commonStaffName;
    }

    public String getCommonStaffSex() {
        return CommonStaffSex;
    }

    public void setCommonStaffSex(String commonStaffSex) {
        CommonStaffSex = commonStaffSex;
    }

    public String getCommonStaffPhone() {
        return CommonStaffPhone;
    }

    public void setCommonStaffPhone(String commonStaffPhone) {
        CommonStaffPhone = commonStaffPhone;
    }
}
