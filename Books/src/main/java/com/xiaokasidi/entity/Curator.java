package com.xiaokasidi.entity;

public class Curator {
    private Integer curatorId;
    private String curatorName;
    private String curatorSex;
    private String curatorPhone;
    private String curatorMailbox;

    public Integer getCuratorId() {
        return curatorId;
    }

    public void setCuratorId(Integer curatorId) {
        this.curatorId = curatorId;
    }

    public String getCuratorName() {
        return curatorName;
    }

    public void setCuratorName(String curatorName) {
        this.curatorName = curatorName;
    }

    public String getCuratorSex() {
        return curatorSex;
    }

    public void setCuratorSex(String curatorSex) {
        this.curatorSex = curatorSex;
    }

    public String getCuratorPhone() {
        return curatorPhone;
    }

    public void setCuratorPhone(String curatorPhone) {
        this.curatorPhone = curatorPhone;
    }

    public String getCuratorMailbox() {
        return curatorMailbox;
    }

    public void setCuratorMailbox(String curatorMailbox) {
        this.curatorMailbox = curatorMailbox;
    }
}
